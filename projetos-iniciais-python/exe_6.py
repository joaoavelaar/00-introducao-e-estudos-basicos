codigo_origem = int(input ("Digite o codigo: "))

if (codigo_origem == 1 or codigo_origem == 2):
    print ("Sul")

if (codigo_origem == 3 or codigo_origem == 4):
    print ("Sudeste")

if (codigo_origem == 5 or codigo_origem == 6):
    print ("Norte")

if (codigo_origem == 7 or codigo_origem == 8):
    print ("Nordeste")

if (codigo_origem == 9 or codigo_origem == 10):
    print ("Centro-Oeste")
